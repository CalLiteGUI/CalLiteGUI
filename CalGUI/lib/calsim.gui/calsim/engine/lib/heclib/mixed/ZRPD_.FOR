      SUBROUTINE ZRPD_  (IFLTAB, CPATH, NORD, NCURVE, IHORIZ,
     * C1UNIT, C1TYPE, C2UNIT, C2TYPE, VALUES, KVALS, NVALS,
     * CLABEL, KLABEL, LABEL, HEADU, HKEADU, NHEADU, ISTAT,
     * L_CPATH, L_C1UNIT, L_C1TYPE, L_C2UNIT, L_C2TYPE, L_CLABEL)
C
      INTEGER   IFLTAB !![REFERENCE]
      CHARACTER CPATH  !![REFERENCE]
      INTEGER   NORD   !![REFERENCE]
      INTEGER   NCURVE !![REFERENCE]
      INTEGER   IHORIZ !![REFERENCE]
      CHARACTER C1UNIT !![REFERENCE]
      CHARACTER C1TYPE !![REFERENCE]
      CHARACTER C2UNIT !![REFERENCE]
      CHARACTER C2TYPE !![REFERENCE]
      REAL      VALUES !![REFERENCE]
      INTEGER   KVALS  !![REFERENCE]
      INTEGER   NVALS  !![REFERENCE]
      CHARACTER CLABEL !![REFERENCE]
      INTEGER   KLABEL !![REFERENCE]
*     LOGICAL   LABEL  !![REFERENCE]  (Really a logical!)
      INTEGER   LABEL  !![REFERENCE]
      INTEGER   HEADU  !![REFERENCE]
      INTEGER   HKEADU !![REFERENCE]
      INTEGER   NHEADU !![REFERENCE]
      INTEGER   ISTAT  !![REFERENCE]
      INTEGER L_CPATH  !![VALUE]
      INTEGER L_C1UNIT !![VALUE]
      INTEGER L_C1TYPE !![VALUE]
      INTEGER L_C2UNIT !![VALUE]
      INTEGER L_C2TYPE !![VALUE]
      INTEGER L_CLABEL !![VALUE]
C
C
C     Initialize return logicals to zero, because of MS Fortran
C     interface problem (only 1 byte is set)
      LABEL = 0
C
      CALL ZRPD ( IFLTAB, CPATH(1:L_CPATH), NORD, NCURVE, IHORIZ,
     * C1UNIT(1:L_C1UNIT), C1TYPE(1:L_C1TYPE), C2UNIT(1:L_C2UNIT),
     * C2TYPE(1:L_C2TYPE), VALUES, KVALS, NVALS, CLABEL(1:L_CLABEL),
     * KLABEL, LABEL, HEADU, HKEADU, NHEADU, ISTAT)
C
       RETURN
       END
